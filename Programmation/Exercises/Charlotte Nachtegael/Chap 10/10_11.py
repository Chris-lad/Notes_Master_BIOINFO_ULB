def print_mat(mat):
    a = len(mat)
    b = len(mat[0])
    for i in range(a):
        for j in range(b):
            print(mat[i][j], end=" ")
        print()

def remove(t, i, j):
    line = len(t)
    col = len(t[0])
    x = t[i][j]
    t[i][j] = " "
    if 0 <= i-1 < line:
        if t[i-1][j] == x:
            remove(t, i-1, j)
    if 0 <= j-1 < col:
        if t[i][j-1] == x:
            remove(t, i, j-1)
    if 0 <= i+1 < line:
        if t[i+1][j] == x:
            remove(t,i+1,j)
    if 0 <= j+1 < col:
        if t[i][j+1] == x:
            remove(t,i,j+1)
    return t

